public class Puripongclass4_3 {
    public static void main(String[] args) {
        int oo = 0;
        System.out.println("");
        for (int p = 1; p <= 10; p++) {
            oo += p;

        }
        System.out.println("คำตอบของ การใช้วนลูป for : " + oo);

        int o = 1;
        int ooo = 0;
        while (o < 11) {
            ooo += o;
            o++;
        }
        System.out.println("คำตอบของ การใช้วนลูปของ while : " + ooo);
    }
}
